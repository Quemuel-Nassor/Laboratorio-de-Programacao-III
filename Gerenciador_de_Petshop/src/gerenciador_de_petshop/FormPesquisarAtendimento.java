    public FormPesquisarAtendimento() {
        initComponents();
        //leitura do arquivo Json que armazena as senhas, usuários e respostas de perguntas de segurança
        try {
            JsonReader reader = new JsonReader(new FileReader(System.getProperty("user.home") + "\\Desktop\\Atendimentos.json"));
            
            reader.beginArray();
            while(reader.hasNext()){
                Atendimento atd = new Gson().fromJson(reader,Atendimento.class);
                atendimentos.add(atd);
            }
        } catch (Exception e) {
            System.out.println("ERRO: "+e.getMessage());
            
        }
        configurarListarAtendimentos();
    }

    void preencherTabela(){
        if(tableatendimentos.getColumnCount() == 0){
            tableatendimentos.addColumn("Cliente");
            tableatendimentos.addColumn("Data");
            tableatendimentos.addColumn("Categoria");
            tableatendimentos.addColumn("Valor");
        }
        
        for (Atendimento atendimento : atendimentos) {
            tableatendimentos.addRow(new Object[]{atendimento.getCliente(),atendimento.getData(),atendimento.getCategoria(),atendimento.getValor()});
        }
        tblAtendimentos.setModel(tableatendimentos);
    }
    
