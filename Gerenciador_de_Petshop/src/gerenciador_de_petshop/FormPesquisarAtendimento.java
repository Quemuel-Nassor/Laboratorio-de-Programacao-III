    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int i = atendimentos.size()-1;
        List<Atendimento> pesquisar = new ArrayList();
        if(tableatendimentos.getRowCount() != 0){
            for(int u = i;u >= 0;u--){
                tableatendimentos.removeRow(u);
            }
        }else if(tableatendimentos.getRowCount() == 0){
            preencherTabela();
        }
        
        for(Atendimento atdm : atendimentos){
            if(txtPesquisaAtendimento.getText().isEmpty()){
                pesquisar.add(atdm);
            }else if(atdm.getCliente().toString().equals(txtPesquisaAtendimento.getText().toString())|| atdm.getData().toString().equals(txtPesquisaAtendimento.getText().toString())){
                pesquisar.add(atdm);
            }
        }
        txtPesquisaAtendimento.setText("");
        for (Atendimento atendimento : pesquisar) {
            tableatendimentos.addRow(new Object[]{atendimento.getCliente(),atendimento.getData(),atendimento.getCategoria(),atendimento.getValor()});
        }
        tblAtendimentos.setModel(tableatendimentos);
        
    }//GEN-LAST:event_btnBuscarActionPerformed

