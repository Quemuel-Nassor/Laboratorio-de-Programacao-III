    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //Adicionar o novo aluno à lista
        FormPrincipal.pedidos.add(
            new Pedido(
                txtCliente.getText(),
                cbxVendedor.getSelectedItem().toString(), 
                cbxProduto.getSelectedItem().toString(), 
                txtQtd.getText(),
                String.format("%s",(Double.parseDouble(txtQtd.getText())*Integer.parseInt(txtValor.getText())))
            )    
        );
        JOptionPane.showMessageDialog(null,"Operação realizada com sucesso!");
        this.dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

