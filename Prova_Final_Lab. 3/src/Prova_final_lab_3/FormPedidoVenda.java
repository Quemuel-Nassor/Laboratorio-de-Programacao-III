    void preencherComboCategoria(){
        String categoria[] = new String[]{"João Alves de Souza","Rodrigo Plotz","Helena Maria Oliveira"};
        DefaultComboBoxModel modelo = new DefaultComboBoxModel(categoria);
        cbxVendedor.setModel(modelo);
        cbxVendedor.setSelectedIndex(0);
        
    } 
    
    void preencherComboProduto(){
        String categoria[] = new String[]{"Anel de vedação","Bobina","Filro de Ar","Rolamento","Jogo de vela"};
        DefaultComboBoxModel modelo = new DefaultComboBoxModel(categoria);
        cbxProduto.setModel(modelo);
        cbxProduto.setSelectedIndex(0);
        
    } 
    private void configurarFormulario(){
        this.setTitle("Cadastro de pedidos");
        this.setResizable(false);
        this.setMaximizable(true);
        this.setClosable(true);
        this.setIconifiable(true);
        preencherComboCategoria();
        preencherComboProduto();
    }
    
