    private void configurarFormulario(){
        this.setTitle("Cadastro de Clientes");
        this.setResizable(false);
        this.setLocationRelativeTo(null);
        
        //iniciar com a guia cadastrar selecionada
        tabPrincipal.setSelectedIndex(0);
        
        preencherComboSexo();
        preencherComboEscolaridade();
        preencherListaProfissoes();
        configurarTabela();
        txtId.setEditable(false);
        
        //criar a lista dinâmica de clientes
        dados = new ArrayList<>();
    }
    
    void preencherComboSexo(){
        DefaultComboBoxModel m = new DefaultComboBoxModel();
        m.addElement("Masculino");
        m.addElement("Feminino");
        cbxSexo.setModel(m);
    }   
    
    void preencherComboEscolaridade(){
        String escolaridade[] = new String[]{"Ensino Fundamental","Ensino médio","Ensino Superior","Especialização","Mestrado","Doutorado"};
        DefaultComboBoxModel m = new DefaultComboBoxModel(escolaridade);
        cbxEscolaridade.setModel(m);
        
        //definir o Ensino Superior como selecionado:
        cbxEscolaridade.setSelectedIndex(2);
    } 
    
    void preencherListaProfissoes(){
        String profissoes[] = new String[]{
	"Astronomia",
	"Banco de Dados",
	"Ciência da Computação",
	"Ciência e Tecnologia",
	"Computação",
	"Estatística",
	"Física",
	"Gestão da Tecnologia da Informação",
	"Informática Biomédica",
	"Matemática",
	"Nanotecnologia",
	"Química",
	"Redes de Computadores",
	"Segurança da Informação",
	"Sistemas de Informação",
	"Sistemas para Internet","Engenharia da Computação"
        };
        
        //ordenação 
        List<String> lista = new ArrayList<>();
        for (String s : profissoes) {
            lista.add(s);
        }
        Collections.sort(lista);
        
        DefaultListModel m = new DefaultListModel();
        for (String u : lista){
            m.addElement(u);
        }
        lstProfissao.setModel(m);
            
        //Definir o modo de múltiplas seleções
        lstProfissao.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
        }
        
