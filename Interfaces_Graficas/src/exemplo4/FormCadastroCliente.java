    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
        
        try{
            JsonWriter writer = new JsonWriter(new FileWriter(System.getProperty("user.home") + "\\Desktop\\Dados.json"));
            
            writer.beginArray();
            for (Cliente cli: dados) {
                new Gson().toJson(cli, Cliente.class,writer);
            }
            writer.endArray();
            writer.close();
            
        }catch(Exception e){
            System.out.println("ERRO: "+ e.getMessage());
        }
        
        
    }//GEN-LAST:event_btnExportarActionPerformed

    private void btnImportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportarActionPerformed
        
        try {
            JsonReader reader = new JsonReader(new FileReader(System.getProperty("user.home") + "\\Desktop\\Dados.json"));
            
            configurarTabela();
            DefaultTableModel m = (DefaultTableModel)
                tblClientes.getModel();
            
            dados = new ArrayList<>();
            reader.beginArray();
            while(reader.hasNext()){
                Cliente cli = new Gson().fromJson(reader,Cliente.class);
                dados.add(cli);
                
                m.addRow(new Object[]{
                    cli.getId(),cli.getNome(),cli.getSexo(),
                    cli.getEscolaridade(),cli.getProfissao(),
                    cli.getTelefone(),cli.getTelefonecelular(),
                    cli.getEmail()
                });
            }
        } catch (Exception e) {
            System.out.println("ERRO: "+e.getMessage());
        }
    }//GEN-LAST:event_btnImportarActionPerformed

