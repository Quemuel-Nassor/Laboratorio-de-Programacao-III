    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        //Adicionar dados na tabela
        DefaultTableModel m = (DefaultTableModel)tblClientes.getModel();
        int lin = 0;
        if(txtId.getText().isEmpty()){ 
            //adicionar
            m.addRow(new Object[]{            
                m.getRowCount()+ 1,
                txtNome.getText(),
                cbxSexo.getSelectedItem(),
                cbxEscolaridade.getSelectedItem(),
                lstProfissao.getSelectedValue(),
                txtTelefone.getText(),
                txtTelefoneCelular.getText(),
                txtEmail.getText()
            });
            lin = m.getRowCount()-1;
        }else{
            //alterar
            while(!m.getValueAt(lin,0).toString().equals(txtId.getText())){
                lin++;
            }
            m.setValueAt(txtNome.getText(), lin, 1);
            m.setValueAt(cbxSexo.getSelectedItem(), lin, 2);
            m.setValueAt(cbxEscolaridade.getSelectedItem(), lin, 3);
            m.setValueAt(lstProfissao.getSelectedValue(), lin, 4);
            m.setValueAt(txtTelefone.getText(), lin, 5);
            m.setValueAt(txtTelefoneCelular.getText(), lin, 6);
            m.setValueAt(txtEmail.getText(), lin, 7);
        }
        tblClientes.setModel(m);
        tabPrincipal.setSelectedIndex(1);
        JOptionPane.showMessageDialog(null,"Operação Realizada com sucesso!");
        
        txtNome.setText("");
        cbxSexo.setSelectedIndex(0);
        cbxEscolaridade.setSelectedIndex(0);
        lstProfissao.setSelectedIndex(0);
        txtTelefone.setText("");
        txtTelefoneCelular.setText("");
        txtEmail.setText("");
        
        //adicionar dados à lista dinâmica
        Cliente cli = new Cliente(
            m.getValueAt(lin, 0) .toString(),
            m.getValueAt(lin, 1) .toString(),
            m.getValueAt(lin, 2) .toString(),
            m.getValueAt(lin, 3) .toString(),
            m.getValueAt(lin, 4) .toString(),
            m.getValueAt(lin, 5) .toString(),
            m.getValueAt(lin, 6) .toString(),
            m.getValueAt(lin, 7) .toString()
        );
        
        if(dados.size() < m.getRowCount()){
           dados.add(cli);//adicionar um novo
        }else{
           dados.set(lin,cli);//alterar 
        }
           
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        
        int lin = tblClientes.getSelectedRow();
        if(lin >= 0){
            txtId.setText(tblClientes.getValueAt(lin,0).toString());
            txtNome.setText(tblClientes.getValueAt(lin, 1).toString());
            cbxSexo.setSelectedItem(tblClientes.getValueAt(lin,2));
            cbxEscolaridade.setSelectedItem(tblClientes.getValueAt(lin,3));
            lstProfissao.setSelectedValue(tblClientes.getValueAt(lin,4),true);
            txtTelefone.setText(tblClientes.getValueAt(lin,5).toString());
            txtTelefoneCelular.setText(tblClientes.getValueAt(lin,6).toString());
            txtEmail.setText(tblClientes.getValueAt(lin,7).toString());
                     
            tabPrincipal.setSelectedIndex(0);
        }
    }//GEN-LAST:event_tblClientesMouseClicked

